const setLang = (lang) => {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((element) => {
    const translateKey = element.getAttribute("data-translate");
    element.textContent = translate[lang][translateKey];
  });
  if (lang === "ar") {
    document.dir = "rtl";
  } else {
    document.dir = "ltr";
  }
}

